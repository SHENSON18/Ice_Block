#pragma once
#include "Draw.h"
#include <list>

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Manager for All GameObjects that have been Register
/// 			To the DrawRegistery through RegisterDraw(). </summary>
///
/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
////////////////////////////////////////////////////////////////////////////////////////////////////

class DrawManager
{
private:

	/// <summary>	The draw registery used for GameObjects wanting to be Drawn. </summary>
	std::list<Draw*> DrawRegistery;

	/// <summary>	A iterator used in the Draw Registery. </summary>
	std::list<Draw*>::iterator iter;
public:

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Destructor. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	virtual ~DrawManager();

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Registers this object. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	///
	/// <param name="pointer">	[in,out] If non-null, the pointer. </param>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void Register(Draw* pointer);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	De register. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	///
	/// <param name="pointer">	[in,out] If non-null, the pointer. </param>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void DeRegister(Draw* pointer);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Draw all. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void DrawAll();

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Cleanups this object. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void Cleanup();

};

