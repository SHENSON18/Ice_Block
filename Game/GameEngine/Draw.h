////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Draw is a inherited State that GameObject has.
/// 			This state works with the DrawManager to Draw all Registered Objects </summary>
///
/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
////////////////////////////////////////////////////////////////////////////////////////////////////

class Draw
{
public:

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Default constructor. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	Draw(){};

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Destructor. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	virtual ~Draw(){};

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Draw object. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	virtual void DrawObject();

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Registers the draw. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void RegisterDraw();

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Used to take GameObject out of DrawRegistry. </summary>
	///
	/// <remarks>	Theonlyhunter, 3/13/2015. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void DeRegisterDraw();
};

